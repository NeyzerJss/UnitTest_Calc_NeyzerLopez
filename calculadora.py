class Calculadora:
    #metodo para sumar 2 modulos
    def suma (self, a, b):
        return a + b
    #metodo para restar 2 modulos
    def resta (self, a, b):
        return a - b
    #metodo para multiplicar 2 modulos
    def multiplicacion (self, a, b):
        return a * b
    #metodo para dividir 2 modulos
    def divisor (self, a, b):
        return a / b